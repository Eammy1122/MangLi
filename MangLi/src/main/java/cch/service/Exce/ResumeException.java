package cch.service.Exce;

public class ResumeException extends RuntimeException {
    public ResumeException() {

    }

    public ResumeException(String arg0) {
        super(arg0);

    }

    public ResumeException(Throwable arg0) {
        super(arg0);

    }

    public ResumeException(String arg0, Throwable arg1) {
        super(arg0, arg1);

    }

    public ResumeException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }
}
